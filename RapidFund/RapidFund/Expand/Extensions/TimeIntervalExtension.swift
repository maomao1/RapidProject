import Foundation

extension DispatchTimeInterval {
    //秒
    static let seconds_0 = DispatchTimeInterval.seconds(0)
    static let seconds_1 = DispatchTimeInterval.seconds(1)
    static let seconds_3 = DispatchTimeInterval.seconds(3)
    static let seconds_5 = DispatchTimeInterval.seconds(5)
    static let seconds_3600 = DispatchTimeInterval.seconds(3600)
    //毫秒
    static let milliseconds_0 = DispatchTimeInterval.milliseconds(0)
    static let milliseconds_100 = DispatchTimeInterval.milliseconds(100)
    static let milliseconds_500 = DispatchTimeInterval.milliseconds(500)
    static let milliseconds_800 = DispatchTimeInterval.milliseconds(800)
    
}
